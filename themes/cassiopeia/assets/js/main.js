// Transparent navbar
(function(){
  const navEl = document.querySelector('#site-navbar');
  if (!navEl) return;

  const hasCover = navEl.getAttribute('data-has-cover') === 'true';
  const threshold = 90; // px scroll before switching styles

  // Non-cover pages: no JS needed; theme colors come from CSS/markup
  if (!hasCover) return;

  // Cover pages: start transparent + light (navbar-dark), switch on scroll
  const onScroll = () => {
    const scrolled = window.scrollY > threshold;
    if (scrolled) {
      navEl.classList.add('navbar-scrolled');
      navEl.classList.remove('navbar-dark');
    } else {
      navEl.classList.remove('navbar-scrolled');
      navEl.classList.add('navbar-dark');
    }
  };

  window.addEventListener('scroll', onScroll, { passive: true });
  onScroll();
})();